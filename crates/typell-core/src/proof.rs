#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_add_refinement_obligation() {
        let mut collector = ObligationCollector::new();
        let pred = Predicate::Gt(Term::Var("x".to_string()), Term::Lit(0));
        collector.add_refinement(pred, "Int");
        assert_eq!(collector.pending().len(), 1);
        assert_eq!(collector.all()[0].id, "PO-0000");
    }

    #[test]
    fn test_discharge_obligation() {
        let mut collector = ObligationCollector::new();
        let pred = Predicate::Gt(Term::Var("x".to_string()), Term::Lit(0));
        collector.add_refinement(pred, "Int");
        assert!(collector.discharge("PO-0000"));
        assert_eq!(collector.pending().len(), 0);
    }

    #[test]
    fn test_term_equality_obligation() {
        let mut collector = ObligationCollector::new();
        collector.add_term_equality(
            Term::Var("n".to_string()),
            Term::BinOp {
                op: crate::types::TermOp::Add,
                lhs: Box::new(Term::Var("a".to_string())),
                rhs: Box::new(Term::Var("b".to_string())),
            },
            "Vec (n) Int",
        );
        assert_eq!(collector.pending().len(), 1);
    }

    #[test]
    fn test_session_completeness_obligation() {
        let mut collector = ObligationCollector::new();
        collector.add_session_completeness("BuyProtocol");
        assert_eq!(collector.pending().len(), 1);
        assert!(matches!(
            collector.all()[0].proposition,
            Proposition::SessionComplete(_)
        ));
    }

    #[test]
    fn test_dimension_check_obligation() {
        let mut collector = ObligationCollector::new();
        collector.add_dimension_check("multiply", "energy", "time");
        assert_eq!(collector.pending().len(), 1);
    }

    // ========================================================================
    // SMT refinement decision procedure tests
    // ========================================================================

    #[test]
    fn test_eval_predicate_constant_true() {
        // 5 > 0 is trivially true
        let pred = Predicate::Gt(Term::Lit(5), Term::Lit(0));
        assert_eq!(eval_predicate(&pred), PredicateResult::True);
    }

    #[test]
    fn test_eval_predicate_constant_false() {
        // 3 > 10 is trivially false
        let pred = Predicate::Gt(Term::Lit(3), Term::Lit(10));
        assert_eq!(eval_predicate(&pred), PredicateResult::False);
    }

    #[test]
    fn test_eval_predicate_variable_unknown() {
        // x > 0 cannot be determined statically
        let pred = Predicate::Gt(Term::Var("x".to_string()), Term::Lit(0));
        assert_eq!(eval_predicate(&pred), PredicateResult::Unknown);
    }

    #[test]
    fn test_eval_predicate_and_both_true() {
        let pred = Predicate::And(
            Box::new(Predicate::Gt(Term::Lit(5), Term::Lit(0))),
            Box::new(Predicate::Lt(Term::Lit(5), Term::Lit(256))),
        );
        assert_eq!(eval_predicate(&pred), PredicateResult::True);
    }

    #[test]
    fn test_eval_predicate_contradiction() {
        // x > 5 && 3 > 10 — second conjunct is false
        let pred = Predicate::And(
            Box::new(Predicate::Gt(Term::Var("x".to_string()), Term::Lit(5))),
            Box::new(Predicate::Gt(Term::Lit(3), Term::Lit(10))),
        );
        assert_eq!(eval_predicate(&pred), PredicateResult::False);
    }

    #[test]
    fn test_eval_predicate_or_one_true() {
        let pred = Predicate::Or(
            Box::new(Predicate::Gt(Term::Lit(3), Term::Lit(10))), // false
            Box::new(Predicate::Gt(Term::Lit(5), Term::Lit(0))),  // true
        );
        assert_eq!(eval_predicate(&pred), PredicateResult::True);
    }

    #[test]
    fn test_eval_predicate_not() {
        let pred = Predicate::Not(
            Box::new(Predicate::Gt(Term::Lit(3), Term::Lit(10))),
        );
        assert_eq!(eval_predicate(&pred), PredicateResult::True);
    }

    #[test]
    fn test_try_discharge_refinements() {
        let mut collector = ObligationCollector::new();
        // 5 > 0 — should be auto-discharged
        collector.add_refinement(
            Predicate::Gt(Term::Lit(5), Term::Lit(0)),
            "Int",
        );
        // x > 0 — cannot be auto-discharged
        collector.add_refinement(
            Predicate::Gt(Term::Var("x".to_string()), Term::Lit(0)),
            "Int",
        );
        let discharged = collector.try_discharge_refinements();
        assert_eq!(discharged, 1);
        assert_eq!(collector.pending().len(), 1);
    }

    #[test]
    fn test_try_discharge_term_equality() {
        let mut collector = ObligationCollector::new();
        let t = Term::Lit(5);
        collector.add_term_equality(t.clone(), t.clone(), "Vec 5 Int");
        let discharged = collector.try_discharge_refinements();
        assert_eq!(discharged, 1);
        assert_eq!(collector.pending().len(), 0);
    }
}
